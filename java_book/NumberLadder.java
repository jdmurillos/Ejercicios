class NumberLadder {
    public static void main(String[] args) {
        
        int x;

        for(x=0 ; x<6 ; x++){

            if(x==1)
                System.out.println("x is one " + x);
            else if (x == 2) 
                System.out.println("x is two " + x);
            else if (x == 3)
                System.out.println("x is three " + x);
            else if (x == 4)
                System.out.println("x is four " + x);
            else
                System.out.println("The number is not between 1 and 5");        
            

        }

    }
}
